$(document).ready(function(){
	hideDescription();
	$('li').on('click',function(){
		var lista = $(this).text();
		$('button').text(lista);
		if(lista == "Todas"){
			$('.comedia').add('.familiar').add('.terror').add('.criminal').add('.accion').show()
		}
		if(lista == "Acci√≥n"){
			$('.comedia').add('.familiar').add('.terror').add('.criminal').hide()
			$('.accion').show()
		}
		if(lista == "Comedia"){
			$('.accion').add('.familiar').add('.terror').add('.criminal').hide()
			$('.comedia').show()
		}
		if(lista == "Crimen"){
			$('.accion').add('.familiar').add('.terror').add('.comedia').hide()
			$('.criminal').show()
		}
		if(lista == "Familiar"){
			$('.accion').add('.criminal').add('.terror').add('.comedia').hide()
			$('.familiar').show()
		}
		if(lista == "Terror"){
			$('.comedia').add('.familiar').add('.accion').add('.criminal').hide()
			$('.terror').show()
		}
	})
	
})
function hide (){
    $('header h1').remove();
    $('.cuadro').hide()
    $('header .container').html('<h1>CINE ALIVANE <i class="fa fa-times" aria-hidden="true"></i></h1>');
	$('.movies').hide();
	$('i').click(function(){
		hideDescription()
		$('header .container').html('<h1> CINE ALIVANE </h1>');
		$('.movies').show();
		$('.cuadro').show();
	})
}
function hideDescription(){
	$('.descriptionAccion').add('.descriptionComedia').add('.descriptionCrimen').add('.descriptionFamiliar').add('.descriptionTerror').hide();
}

